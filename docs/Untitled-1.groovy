                                          Instagram :
 
 
a)     Le nombre d'utilisateurs d'Instagram est de 1,395 milliards d’utilisateurs actifs.
 
b)    Le nombre d’utilisateurs a beaucoup augmenté ces dernières années. En décembre 2016 : 600 millions, en septembre 2017 : 800 millions et en juin 2018 : 1 milliard. 
 
c)     Leur principale sources de revenus stable est la vente d'espace publicitaire présent sur chaque compte – ce qui conduit directement à certains aspects de la problématique : la revente d'informations personnelles pour effectuer un ciblage publicitaire précis, ou les fuites de données.
 
d)    Oui on peut régler les paramètres de confidentialités sur Instagram même si cela reste tout de même difficile. 
 
e)     Oui il est tout à fait possible de se désinscrire, lorsqu’on se désinscrit, le compte n’est pas supprimé, il est juste désactivé pendant un mois complet puis il disparait. 
 
f)      Oui le réseau social efface toute les données personnelles d’un utilisateur qui se désinscrit, il les supprime définitivement de son serveur. 
 
 
                                                  Snapchat : 
 
a)     En 2022, Il compte 347 millions d'utilisateurs actifs quotidiens et 500 millions d'utilisateurs actifs mensuels.
 
b)    Le nombre d’utilisateurs sur Snapchat a bien augmenté cette année on est passée de 238 millions d’utilisateurs actifs au second semestre 2020, 293 millions au deuxième trimestre de l’année 2021 contre 500 millions d’utilisateurs actifs cette année 2022. 
 
c)    99% des revenus de Snapchat ont été générés par la publicité en 2020 contre 98% en 2019; contre 99% en 2018 et 97% en 2017 selon le rapport annuel de Snapchat Inc.
 
d)    Oui on peut régler les paramètres de confidentialités en appuyant sur « Contrôles de confidentialités » dans le boulon en allant sur le compte. 
 
e)    Il est tout à fait possible de se désinscrire, comme pour Instagram le compte se désactivé pendant une certaine durée. 
 
f)     Il faut suivre ce tutoriel pour pouvoir supprimer les données personnelles définitivement de Snapchat : 
 
         Comment vider mon cache sur Snapchat ?
   Appuyez sur ⚙️ dans Mon profil pour ouvrir les Réglages.
        Faites défiler l'écran vers le bas et appuyez sur "Vider le cache"
        Appuyez sur "Tout effacer" sous iOS, ou sur "Continuer" sous Android.
 
 
                                                                      

 
